const groupDataByYear = (data) => {
  return data
    .reduce((acc, item) => {
      const yearIndex = item.year - 1;
      if (!acc[yearIndex]) {
        acc[yearIndex] = { year: item.year, semesters: [] };
      }
      acc[yearIndex].semesters.push(item);
      return acc;
    }, [])
    .filter((item) => item !== null);
};

